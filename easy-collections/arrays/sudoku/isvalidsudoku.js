/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
  for (var y=0;y<9;y++) {
    var nums = [];
    for (var x=0;x<9;x++) {
      if (board[y][x] !== ".") {
        if (nums.indexOf(board[y][x]) >= 0) {
          return false;
        }
        nums.push(board[y][x]);
      }
    }
  }
    
  for (var x=0;x<9;x++) {
    var nums = [];
    for (var y=0;y<9;y++) {
      if (board[y][x] !== ".") {
        if (nums.indexOf(board[y][x]) >= 0) {
          return false;
        }
        nums.push(board[y][x]);
      }
    }
  }

  for (var sx=0;sx<3;sx++) {
    for (var sy=0;sy<3;sy++) {
      var nums = [];
      for (var x=0;x<3;x++) {
        for (var y=0;y<3;y++) {
          var b = board[sy*3+y][sx*3+x];
          if (b !== ".") {
            if (nums.indexOf(b) >= 0) {
              return false;
            }
            nums.push(b);
          }
        }
      }
    }
  }
  return true;
};

