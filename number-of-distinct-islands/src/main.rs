use std::collections::HashSet;

impl Solution {
    fn dfs(
        grid: &[Vec<i32>],
        x: i32,
        y: i32,
        ori_x: i32,
        ori_y: i32,
        set: &mut HashSet<(i32, i32)>,
    ) {
        let dx = [-1, 0, 1, 0];
        let dy = [0, -1, 0, 1];
        let xlen = grid.len() as i32;
        let ylen = grid[0].len() as i32;
        set.insert((x - ori_x, y - ori_y));
        for i in 0..4 {
            let x_n = x + dx[i];
            let y_n = y + dy[i];
            if x_n >= 0 && x_n < xlen && y_n >= 0 && y_n < ylen {
                let item = grid[x_n as usize][y_n as usize];
                if item == 1 && !set.contains(&(x_n - ori_x, y_n - ori_y)) {
                    Solution::dfs(grid, x_n, y_n, ori_x, ori_y, set);
                }
            }
        }
    }
    pub fn num_distinct_islands(grid: Vec<Vec<i32>>) -> i32 {
        let xlen = grid.len();
        let ylen = grid[0].len();
        let mut block: HashSet<String> = HashSet::new();
        let mut visited: HashSet<(i32, i32)> = HashSet::new();
        for x in 0..xlen {
            for y in 0..ylen {
                let m = grid[x as usize][y as usize];
                if m == 1 && !visited.contains(&(x as i32, y as i32)) {
                    visited.insert((x as i32, y as i32));

                    let mut set: HashSet<(i32, i32)> = HashSet::new();
                    Solution::dfs(&grid, x as i32, y as i32, x as i32, y as i32, &mut set);
                    // println!("{:?}, x = {}, y = {}", set, x, y);
                    for (px, py) in set.iter() {
                        visited.insert((x as i32 + px, y as i32 + py));
                    }
                    let mut v: Vec<(i32, i32)> = set.iter().cloned().collect();
                    v.sort_unstable();
                    let str = v.iter().fold(String::new(), |acc, x| {
                        acc + format!("({}, {})", x.0, x.1).as_str()
                    });
                    block.insert(str);
                }
            }
        }
        block.len() as i32
    }
}

struct Solution;

#[macro_export]
macro_rules! grid {
    ( $([$( $x:expr ),*]),* ) => {
        {
            vec![
                $(
                    vec![$($x), *],
                )*
            ]
        }
    };
}

